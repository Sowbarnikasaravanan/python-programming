'''
Bitwise Operators
&  ->Bitwise and
|  ->Bitwise or
~  ->Bitwise not  
<< ->Left shift
>> ->Right shift
'''
a=int(input("Enter the first number:"))
b=int(input("Enter the second number:"))
print(a&b)
print(a|b)
print(~a)
print(a<<2)
print(b>>2)

