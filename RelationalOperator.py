'''
Relational Operators
<    ->Less than
>    ->Greater than
<=   ->Less than or equal to
>=   ->Greater than or equal to
==   ->equal to
!=   ->not equal to
'''
a=int(input("Enter the first number:"))
b=int(input("Enter the second number:"))
print(a<b)
print(a>b)
print(a<=b)
print(a>=b)
print(a==b)
print(a!=b)

